    <h3 id="item1">Assistência Social</h3>
    <p>A Política Municipal de Assistência Social tem por funções a proteção social, a vigilância socioassistencial e a defesa de direitos. Ocupa-se de prover proteção à vida, reduzir danos, prevenir a incidência de riscos sociais, independente de contribuição prévia. A Assistência Social é organizada sob a forma de sistema público não contributivo, descentralizado e participativo, denominado Sistema Único de Assistência Social (SUAS). A Lei Municipal nº 6.422, de 21 de maio de 2020 regulamenta a Política de Assistência Social em seu âmbito. <a href="https://www.americana.sp.gov.br/legislacao/lei_6422_2020.html">Lei Municipal nº 6.422 (Acesso à outro site)</a></p>

    <h3 class="pt-4" id="item2">Princípios e Diretrizes do SUAS</h3>
    <p>A Norma Operacional Básica do Sistema Único de Assistência Social (NOB/SUAS) regulamentada pela Resolução do Conselho Nacional de Assistência Social (CNAS) ordena os seguintes princípios e diretrizes do SUAS:</p>

    <h4 id="item3">Princípios Organizativos do SUAS (artigo 3º da NOB/SUAS):</h4>
    <ul>
        <li><strong>Universalidade:</strong> todos têm direito à proteção socioassistencial, prestada a quem dela necessitar, com respeito à dignidade e à autonomia do cidadão, sem discriminação de qualquer espécie ou comprovação vexatória da sua condição.</li>
        <li><strong>Gratuidade:</strong> a assistência social deve ser prestada sem exigência de contribuição ou contrapartida, observado o que dispõe o art. 35, da Lei nº 10.741, de 1º de outubro de 2003 – Estatuto do Idoso.</li>
        <li><strong>Integralidade da Proteção Social:</strong> oferta das provisões em sua completude, por meio de conjunto articulado de serviços, programas, projetos e benefícios socioassistenciais.</li>
        <li><strong>Intersetorialidade:</strong> integração e articulação da rede socioassistencial com as demais políticas e órgãos setoriais.</li>
        <li><strong>Equidade:</strong> respeito às diversidades regionais, culturais, socioeconômicas, políticas e territoriais, priorizando aqueles que estiverem em situação de vulnerabilidade e risco pessoal e social.</li>
    </ul>

    <h4 id="item4">Diretrizes Estruturantes da Gestão do SUAS (artigo 5º da NOB/SUAS):</h4>
    <ul>
        <li>Primazia da responsabilidade do Estado na condução da Política de Assistência Social.</li>
        <li>Descentralização político-administrativa e comando único das ações em cada esfera de governo.</li>
        <li>Financiamento partilhado entre a União, os Estados, o Distrito Federal e os Municípios.</li>
        <li>Matricialidade sociofamiliar.</li>
        <li>Territorialização.</li>
        <li>Fortalecimento da relação democrática entre Estado e sociedade civil.</li>
        <li>Controle social e participação popular.</li>
    </ul>

    <h3 class="pt-4" id="item5">Cobetura da Rede Socioassistencial</h3>
    <p>Considera-se rede socioassistencial o conjunto integrado da oferta de SERVIÇOS, PROGRAMAS, PROJETOS e BENEFÍCIOS de Assistência Social mediante a articulação entre todas as unidades do SUAS. Organiza-se pelos seguintes tipos e níveis de proteção social, dividindo-se em ATENDIMENTO, ASSESSORAMENTO e DEFESA E GARANTIA DE DIREITOS. As ofertas socioassistenciais são executadas diretamente pela Secretaria ou pelas Organizações da Sociedade Civil vinculadas ao SUAS, respeitadas as especialidades.</p>

    <h4 id="item6">Serviços, Programas, Projetos e Benefícios:</h4>
    <ul>
        <li><strong>Serviços:</strong> são atividades continuadas que visam à melhoria da vida da população a partir de ações voltadas para o atendimento de suas necessidades básicas, considerando objetivos, princípios e diretrizes estabelecidos na lei. Esses serviços são organizados em rede, de acordo com os níveis de proteção social básica e especial de média e alta complexidade.</li>
        <li><strong>Programas:</strong> são ações integradas e complementares com objetivos, tempo e área de abrangência definidos para qualificar, incentivar, potencializar e melhorar os benefícios e os serviços assistenciais. Não se caracterizando como ações continuadas.</li>
        <li><strong>Projetos:</strong> são investimentos econômico-sociais nos grupos populacionais em situação de pobreza. Buscam subsidiar técnica e financeiramente iniciativas que lhes garantem meios e capacidade produtiva e de gestão. Os projetos integram o nível de proteção social básica, podendo, contudo, voltar-se ainda às famílias em situação de risco, usuários/as da proteção social especial, e podem ser construídos articuladamente com as demais políticas públicas.</li>
        <li><strong>Benefícios:</strong> são provisões financeiras ou materiais, concedidas aos indivíduos por tempo determinado ou de forma continuada, visando cobrir necessidades temporárias ou permanentes relacionadas ao ciclo de vida, às situações de desvantagem social ou à ocorrência de vulnerabilidade e risco social.</li>
    </ul>

    <h4 id="item7">Atendimento, Assessoramento e Defesa e Garantia de Direitos:</h4>
    <ul>
        <li><strong>Atendimento:</strong> prestação de serviços, execução de programas e projetos e concessão de benefícios, de forma continuada, permanente e planejada, de prestação social básica ou especial, dirigidos às famílias e indivíduos em situações de vulnerabilidade ou risco social e pessoal.</li>
        <li><strong>Assessoramento:</strong> prestação de serviços, execução de programas e projetos, de forma continuada, permanente e planejada, voltados prioritariamente para o fortalecimento dos movimentos sociais e das organizações de usuários, formação e capacitação de lideranças, dirigidos ao público da política de assistência social.</li>
        <li><strong>Defesa e Garantia de Direitos:</strong> prestação de serviços, execução de programas e projetos, de forma continuada, permanente e planejada, voltados prioritariamente para a defesa e efetivação dos direitos socioassistenciais, construção de novos direitos, promoção da cidadania, enfrentamento das desigualdades sociais, articulação com órgãos públicos de defesa de direitos, dirigidos ao público da política de assistência social.</li>
    </ul>

