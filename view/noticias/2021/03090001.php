        <p class="pt-4">O prefeito de Americana, Chico Sardelli, está instituindo, por meio do decreto nº 12.640, o Programa para Atenção à População em Situação de Rua. O prefeito e a secretária de Assistência Social e Direitos Humanos, Juliani Hellen Munhoz Fernandes, anunciaram o programa na tarde desta terça-feira (9), durante uma live.</p>

        <p>"É um anúncio muito importante para a cidade de Americana. Temos uma nova proposta para a atenção à pessoa em situação de rua. Queremos oferecer total assistência a essas pessoas que tanto precisam de cuidado e carinho. Agradeço a todos os envolvidos neste projeto, que vai oferecer uma vida digna às pessoas em situação de rua. Espero que a gente consiga com este decreto fazer mais para a população que mais precisa. Parabenizo à secretária Juliani e toda a equipe da assistência social e direitos humanos pelo trabalho", disse o prefeito Chico Sardelli. </p>

        <p>Para a secretária Juliani, a implantação do Programa vai estabelecer uma política de atendimento, para reduzir a vulnerabilidade social da população em situação de rua, gerando uma estratégia de fluxo de trabalho para gerar mais oportunidades e a reinserção social desta população. </p>

        <p>"Faz parte de uma das estratégias do plano de governo do prefeito Chico Sardelli a ampliação das ações de políticas voltadas à população em situação de rua. A proposta é a integração dos serviços municipais e ações da sociedade organizada, estabelecendo uma política de atendimento, diagnóstico da situação do município, criação de um Protocolo de Atendimento e Fluxo de Trabalho para tornar o atendimento mais ágil e efetivo, aprimorando o trabalho já existente dentro dos nossos serviços", disse a secretária de Assistência Social e Direitos Humanos. </p>

        <p>Com a implantação do Programa e de um Comitê Intersetorial, o objetivo será proporcionar a articulação de serviços e o desenvolvimento de ações que estarão inseridos na Política Municipal Intersetorial de Atenção à População em Situação de Rua, uma proposta que será entregue ao prefeito Chico Sardelli num prazo de 100 dias. A Secretaria de Assistência Social e Direitos Humanos coordenará os trabalhos. </p>

        <p>O Programa contempla ações para inserir a população em situação de rua às políticas de saúde, habitação, educação, assistência social, cultura, esporte e lazer; realização de um censo da população em situação de rua no município; elaboração, revisão e pactuação da Política Municipal Intersetorial de Atenção à População em Situação de Rua do Município de Americana; concretizar a elaboração de protocolos e fluxos para atendimento, acompanhamento e monitoramento da execução do Programa; regular e agilizar os processos e procedimentos na atenção qualificada à população em situação de rua; incentivar a participação e articulação da sociedade civil nas ações voltadas a atenção à população em situação de rua. </p>

