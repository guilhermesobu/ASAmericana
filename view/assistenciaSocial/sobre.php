        <h3 class="text-left">Assistência Social</h3>

        <p>A Política Municipal de Assistência Social  tem por funções a proteção social, a vigilância socioassistencial e a defesa de direitos. Ocupa-se de prover proteção à vida, reduzir danos, prevenir a incidência de riscos sociais, independente de contribuição prévia. A Assistência Social é organizada sob a forma de sistema público não contributivo, descentralizado e participativo, denominado Sistema Único de Assistência Social (SUAS). A Lei Municipal nº 6.422, de 21 de maio de 2020 regulamenta a Política de Assistência Social em seu âmbito. <a href="https://www.americana.sp.gov.br/legislacao/lei_6422_2020.html">https://www.americana.sp.gov.br/legislacao/lei_6422_2020.html</a></p>

        <p>A Norma Operacional Básica do Sistema Único de Assistência Social (NOB/SUAS) regulamentada pela Resolução do Conselho Nacional de Assistência Social (CNAS) ordena os objetivos, princípios, seguranças e diretrizes do SUAS que são:</p>

        <p class="mb-0"><strong>Objetivos do SUAS (artigo 2º da NOB/SUAS):</strong></p>
        <ul>
            <li class="pl-2 pr-5">consolidar a gestão compartilhada, o cofinanciamento e a cooperação técnica entre a União, os Estados, o Distrito Federal e os Municípios que, de modo articulado, operam a proteção social não contributiva e garantem os direitos dos usuários;</li>
            <li class="pl-2 pr-5">estabelecer as responsabilidades da União, dos Estados, do Distrito Federal e dos Municípios na organização, regulação, manutenção e expansão das ações de assistência social;</li>
            <li class="pl-2 pr-5">definir os níveis de gestão, de acordo com estágios de organização da gestão e ofertas de serviços pactuados nacionalmente;</li>
            <li class="pl-2 pr-5">orientar-se pelo princípio da unidade e regular, em todo o território nacional, a hierarquia, os vínculos e as responsabilidades quanto à oferta dos serviços, benefícios, programas e projetos de assistência social;</li>
            <li class="pl-2 pr-5">respeitar as diversidades culturais, étnicas, religiosas, socioeconômicas, políticas e territoriais;</li>
            <li class="pl-2 pr-5">reconhecer as especificidades, iniquidades e desigualdades regionais e municipais no planejamento e execução das ações;</li>
            <li class="pl-2 pr-5">assegurar a oferta dos serviços, programas, projetos e benefícios da assistência social;</li>
            <li class="pl-2 pr-5">integrar a rede pública e privada, com vínculo ao SUAS, de serviços, programas, projetos e benefícios de assistência social;</li>
            <li class="pl-2 pr-5">implementar a gestão do trabalho e a educação permanente na assistência social;</li>
            <li class="pl-2 pr-5">estabelecer a gestão integrada de serviços e benefícios;</li>
            <li class="pl-2 pr-5">afiançar a vigilância socioassistencial e a garantia de direitos como funções da política de assistência social.</li>
        </ul>

        <p class="mb-0"><strong>Princípios Organizativos do SUAS (artigo 3º da NOB/SUAS):</strong></p>
        <ul>
            <li class="pl-2 pr-5">universalidade: todos têm direito à proteção socioassistencial, prestada a quem dela necessitar, com respeito à dignidade e à autonomia do cidadão, sem discriminação de qualquer espécie ou comprovação vexatória da sua condição;</li>
            <li class="pl-2 pr-5">gratuidade: a assistência social deve ser prestada sem exigência de contribuição ou contrapartida, observado o que dispõe o art. 35, da Lei nº 10.741, de 1º de outubro de 2003 – Estatuto do Idoso;</li>
            <li class="pl-2 pr-5">integralidade da proteção social: oferta das provisões em sua completude, por meio de conjunto articulado de serviços, programas, projetos e benefícios socioassistenciais;</li>
            <li class="pl-2 pr-5">intersetorialidade: integração e articulação da rede socioassistencial com as demais políticas e órgãos setoriais;</li>
            <li class="pl-2 pr-5">equidade: respeito às diversidades regionais, culturais, socioeconômicas, políticas e territoriais, priorizando aqueles que estiverem em situação de vulnerabilidade e risco pessoal e social.</li>
        </ul>

        <p class="mb-0"><strong>Seguranças Afiançadas pelo SUAS (artigo 4º da NOB/SUAS):</strong></p>
        <ul>
            <li class="pl-2 pr-5"><strong>acolhida</strong>: provida por meio da oferta pública de espaços e serviços para a realização da proteção social básica e especial, devendo as instalações físicas e a ação profissional conter:</li>
            <ol type="a" class="pr-5">
                <li class="pl-2 pr-5">condições de recepção;</li>
                <li class="pl-2 pr-5">escuta profissional qualificada;</li>
                <li class="pl-2 pr-5">informação;</li>
                <li class="pl-2 pr-5">referência;</li>
                <li class="pl-2 pr-5">concessão de benefícios;</li>
                <li class="pl-2 pr-5">aquisições materiais e sociais;</li>
                <li class="pl-2 pr-5">abordagem em territórios de incidência de situações de risco;</li>
                <li class="pl-2 pr-5">oferta de uma rede de serviços e de locais de permanência de indivíduos e famílias sob curta, média e longa permanência.</li>
            </ol>

            <li class="pl-2 pr-5"><strong>renda</strong>: operada por meio da concessão de auxílios financeiros e da concessão de benefícios continuados, nos termos da lei, para cidadãos não incluídos no sistema contributivo de proteção social, que apresentem vulnerabilidades decorrentes do ciclo de vida e/ou incapacidade para a vida independente e para o trabalho;</li>

            <li class="pl-2 pr-5"><strong>convívio ou vivência familiar, comunitária e social</strong>: exige a oferta pública de rede continuada de serviços que garantam oportunidades e ação profissional para:</li>
            <ol type="a" class="pr-5">
                <li class="pl-2 pr-5">a construção, restauração e o fortalecimento de laços de pertencimento, de natureza geracional, intergeracional, familiar, de vizinhança e interesses comuns e societários;</li>
                <li class="pl-2 pr-5">o exercício capacitador e qualificador de vínculos sociais e de projetos pessoais e sociais de vida em sociedade.</li>
            </ol>

            <li class="pl-2 pr-5"><strong>desenvolvimento de autonomia</strong>: exige ações profissionais e sociais para:</li>
            <ol type="a" class="pr-5">
                <li class="pl-2 pr-5">o desenvolvimento de capacidades e habilidades para o exercício do protagonismo, da cidadania;</li>
                <li class="pl-2 pr-5">a conquista de melhores graus de liberdade, respeito à dignidade humana, protagonismo e certeza de proteção social para o cidadão e a cidadã, a família e a sociedade;</li>
                <li class="pl-2 pr-5">conquista de maior grau de independência pessoal e qualidade, nos laços sociais, para os cidadãos e as cidadãs sob contingências e vicissitudes.</li>
            </ol>

            <li class="pl-2 pr-5"><strong>apoio e auxílio</strong>: quando sob riscos circunstanciais, exige a oferta de auxílios em bens materiais e em pecúnia, em caráter transitório, denominados de benefícios eventuais para as famílias, seus membros e indivíduos.</li>

        </ul>

        <p class="mb-0"><strong>Diretrizes Estruturantes da Gestão do SUAS (artigo 5º da NOB/SUAS):</strong></p>
        <ul>
            <li class="pl-2 pr-5">Primazia da responsabilidade do Estado na condução da Política de Assistência Social;</li>
            <li class="pl-2 pr-5">Descentralização político-administrativa e comando único das ações em cada esfera de governo;</li>
            <li class="pl-2 pr-5">Financiamento partilhado entre a União, os Estados, o Distrito Federal e os Municípios;</li>
            <li class="pl-2 pr-5">Matricialidade sociofamiliar;</li>
            <li class="pl-2 pr-5">Territorialização;</li>
            <li class="pl-2 pr-5">Fortalecimento da relação democrática entre Estado e sociedade civil;</li>
            <li class="pl-2 pr-5">Controle social e participação popular.</li>
        </ul>

