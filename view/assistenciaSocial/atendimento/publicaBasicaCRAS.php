            <h4 class="text-left text-primary">Centro de Referência de Assistência Social - CRAS</h4>

            <p>Os CRAS são unidades públicas municipal, de base territorial, localizada em áreas com maiores índices de vulnerabilidade e risco social, destinada à articulação dos serviços socioassistenciais no seu território de abrangência e à prestação de serviços, programas e projetos socioassistenciais de proteção social básica às famílias. Os CRAS ofertam exclusivamente os Serviços PAIF.</p>

            <p>Previsto no artigo 24-A da Lei Orgânica de Assistência Social (LOAS), o Serviço de Proteção e Atendimento Integral à Família (PAIF), integra a proteção social básica e consiste na oferta de ações e serviços socioassistenciais de prestação continuada, por meio do trabalho social com famílias em situação de vulnerabilidade social, com o objetivo de prevenir o rompimento dos vínculos familiares e a violência no âmbito de suas relações, garantindo o direito à convivência familiar e comunitária.</p>

            <p>De acordo com a Tipificação Nacional dos Serviços Socioassistenciais o Serviço PAIF desenvolve o trabalho social com famílias, de caráter continuado, com a finalidade de fortalecer a função protetiva das famílias, prevenir a ruptura dos seus vínculos, promover seu acesso e usufruto de direitos e contribuir na melhoria de sua qualidade de vida.</p>

            <p>Os/as usuários/as do PAIF são famílias em situação de vulnerabilidade social decorrente da pobreza, do precário ou nulo acesso aos serviços públicos, da fragilização de vínculos de pertencimento e sociabilidade e/ou qualquer outra situação de vulnerabilidade e risco social, residentes nos territórios de abrangência dos CRAS, em especial: famílias beneficiárias de programas de transferência de renda e benefícios assistenciais; famílias que atendem os critérios de elegibilidade a tais programas ou benefícios, mas que ainda não foram contempladas; famílias em situação de vulnerabilidade em decorrência de dificuldades vivenciadas por algum de seus membros; pessoas com deficiência e/ou pessoas idosas que vivenciam situações de vulnerabilidade e risco social.</p>

            <p>É através do CRAS que se dá o acesso ao Serviço de Convivência e Fortalecimento de Vínculos (SCFV).</p>

