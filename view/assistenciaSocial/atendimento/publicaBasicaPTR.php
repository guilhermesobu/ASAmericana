            <h4 class="pt-4 text-primary">Bolsa Família:</h4>

            <p class="pt-2 mb-0"><strong>O que é?</strong></p>
            <p>É um programa que contribui para o combate à pobreza e à desigualdade no Brasil. Ele foi criado em outubro de 2003 e possui três eixos principais:</p>
            <ul>
                <li>Complemento da renda: todos os meses, as famílias atendidas pelo Programa recebem um benefício em dinheiro, que é transferido diretamente pelo governo federal. Esse eixo garante o alívio mais imediato da pobreza;</li>
                <li>Acesso a direitos: as famílias devem cumprir alguns compromissos (condicionalidades), que têm como objetivo reforçar o acesso à educação, à saúde e à assistência social. Esse eixo oferece condições para as futuras gerações quebrarem o ciclo da pobreza, graças a melhores oportunidades de inclusão social.</li>
            </ul>

            <p class="pt-2 mb-0"><strong>Quem pode participar?</strong></p>
            <p>O Programa Bolsa Família atende às famílias que vivem em situação de pobreza e de extrema pobreza. Foi utilizado um limite de renda para definir esses dois patamares. Assim, podem fazer parte do Programa:</p>
            <ul>
                <li>Todas as famílias com renda por pessoa de até R$ 89,00 mensais;</li>
                <li>Famílias com renda por pessoa entre R$ 89,01 e R$ 178,00 mensais, desde que tenham crianças ou adolescentes de 0 a 17 anos.</li>
            </ul>

            <p class="pt-2 mb-0"><strong>Como se cadastrar?</strong></p>
            <p>Não existe um cadastro específico do Programa Bolsa Família. Na realidade, quando a pessoa fala que fez o cadastro do Bolsa, ela se inscreveu no Cadastro Único para Programas Sociais do Governo Federal, ou apenas Cadastro Único.</p>


            <h4 class="pt-4 text-primary">Programa Renda Cidadã:</h4>

            <p>É um programa de transferência direta de renda desenvolvido pela Secretaria de Desenvolvimento Social do governo do estado de São Paulo, que proporciona também ações complementares desenvolvidas pelos municípios.</p>
            <p>O programa atende famílias que vivenciam processo de vulnerabilidade em decorrência do desemprego ou subemprego, envolvendo fatores como a ausência de qualificação profissional, o analfabetismo, a situação precária de saúde, a inexistência de moradia ou sua precariedade, a dependência química, entre outros problemas e dificuldades.</p>
            <p>A proposta é enfrentar o processo de empobrecimento de uma parcela significativa da população, que tem alterado profundamente a estrutura da família, seu sistema de relações e os papéis desempenhados.</p>
            <p>Como meta, o Renda Cidadã busca a melhoria na qualidade de vida pela concessão de apoio direto às famílias e promoção de ações complementares. Tem como condicionalidades a frequência escolar das crianças (educação) e o acompanhamento na área de saúde.</p>
            <p>Para acessar o benefício, os interessados buscam o Centro de Referência de Assistência Social (CRAS) do município para verificação dos critérios para inscrição e cadastramento no Cadastro Único.</p>

            <h4 class="pt-4 text-primary">Programa Ação Jovem:</h4>

            <p>O Ação Jovem, programa estadual, tem como objetivo promover a inclusão social de jovens na faixa etária de 15 a 24 anos com renda mensal familiar per capita de até meio salário mínimo nacional, prioritariamente até ¼ do salário mínimo, mediante ações complementares de apoio à iniciação profissional e transferência direta de renda, como apoio financeiro temporário do Estado.</p>
            <p>O programa está presente em 642 municípios, com uma ajuda no valor R$ 80,00 por jovem/ mês com o CPF cadastrado no CadÚnico.</p>
            <p>Os jovens interessados podem procurar o Centro de Referência de Assistência Social (CRAS) para orientações em relação ao cadastramento no Cadastro Único. Jovens já cadastrados devem manter o cadastro atualizado até 24 meses e quando houver quaisquer mudanças nos dados cadastrais.</p>
            <p>De acordo com a Resolução SEDS nº 13, de 03 de agosto de 2015, fica estabelecido que o Cadastro Único para programas sociais, o CadÚnico, regido pelo Decreto Federal nº 6.135, de 26 de junho de 2007, será a referência para seleção de beneficiários dos programas desenvolvidos pela Secretaria de Desenvolvimento Social.</p>

