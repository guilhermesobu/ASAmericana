            <h4 class="text-primary">BENEFÍCIO DE PRESTAÇÃO CONTINUADA (BPC)</h4>
            <p>O Benefício de Prestação Continuada (BPC), previsto na Lei Orgânica da Assistência Social (LOAS), é a garantia de um salário mínimo por mês a pessoa idosa com idade igual ou superior a 65 anos ou à pessoa com deficiência de qualquer idade. No caso da pessoa com deficiência, esta condição tem de ser capaz de lhe causar impedimentos de natureza física, mental, intelectual ou sensorial de longo prazo (com efeitos por pelo menos 2 anos), que a impossibilite de participar de forma plena e efetiva na sociedade, em igualdade de condições com as demais pessoas.</p>
            <p>O BPC não é aposentadoria. Para ter direito a ele, não é preciso ter contribuído para o INSS. Diferente dos benefícios previdenciários, o BPC não paga 13º salário e não deixa pensão por morte.</p>
            <p>Para ter direito ao BPC, é necessário que a renda por pessoa do grupo familiar seja renda inferior (ou menor) a 1/4 do salário mínimo. Além da renda de acordo com o requisito estabelecido, as pessoas com deficiência também passam por avaliação médica e social no Instituto Nacional do Seguro Social (INSS).</p>
            <p>O beneficiário do BPC, assim como sua família, deve estar inscrito no Cadastro Único. Isso deve ser feito antes mesmo de o benefício ser solicitado. Sem isso, ele não pode ter acesso ao BPC.</p>
            <p>O cidadão pode procurar o Centro de Referência de Assistência Social (CRAS) de seu território para receber as informações sobre o BPC e como pode requerê-lo. Para receber o benefício, não é preciso pagar intermediários ou agenciadores.</p>
            <p>O requerimento pode ser realizado nas Agências da Previdência Social (APS) ou por meio dos canais de atendimento do INSS: pelo telefone 135 (ligação gratuita) ou pelo site ou aplicativo de celular “Meu INSS”.</p>

