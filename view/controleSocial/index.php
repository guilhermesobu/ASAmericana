        <h3 class="text-left">Controle Social</h3>
        <p>Devido à organização e mobilização popular realizada na década de 1980, colocou-se em evidência a possibilidade de inversão do controle social, em prol de um estado democrático e garantidor do acesso universal aos direitos sociais. A partir desse contexto, surge a perspectiva de um controle da sociedade civil sobre o Estado, que é incorporada na Constituição Federal de 1988, como uma forma de democratizá-lo.</p>
        <p>A Carta Magna assegurou a existência de órgãos de controle no interior do próprio Estado (como os Tribunais de Contas, as Controladorias, o Ministério Público e o Poder Judiciário), bem como novas possibilidades de controle da sociedade sobre o Estado, diferentes daquelas que ocorrem nos processos eleitorais. Esses são os fundamentos para a criação dos conselhos de políticas e de outros espaços de participação e de controle social que foram surgindo no Brasil, especialmente relacionados a áreas de políticas públicas.</p>
        <p>Um primeiro tipo de controle é exercido por instituições do próprio Estado e ocorre em dois níveis: controle interno, que é exercido por órgãos do Poder Executivo, como as controladorias, e controle externo, que é exercido pelo Ministério Público e pelo Poder Legislativo.</p>
        <p>O controle social é o segundo tipo de controle público que a sociedade exerce sobre seus governantes, podendo ser por meio da opinião pública manifestada em ações de mobilização, nos fóruns ou espaços institucionalizados nos quais se avalia a atuação governamental numa dada área de política pública, por exemplo, como é o caso dos conselhos.</p>
        <p class="mb-0">O Exercício do Controle Social:</p>
        <ul>
            <li>Possibilita a participação da sociedade nos assuntos do governo.</li>
            <li>Democratiza a gestão das políticas públicas.</li>
            <li>Zela pela utilização dos recursos públicos.</li>
            <li>Torna o governo mais transparente e a sociedade civil mais atenta.</li>
        </ul>
