            <p>O Diagnóstico do Sistema de Garantia de Direitos da Pessoa Idosa (SGDPI) é a ferramenta de planejamento que permite avaliar de que modo os problemas se manifestam no Município, apontar as prioridades e mobilizar forças locais para a ação, estimulando a articulação entre o poder público e a sociedade civil em torno da melhoria das condições de vida da população idosa. O produto final a ser alcançado é a reformulação e implantação da Política Municipal da Pessoa Idosa, garantindo a criação de ações até então inexistentes, aprimoramento na rede de atendimento e direcionamento de serviços ou programas nos territórios ou segmentos da população idosa que estiverem descobertos ou vulneráveis.</p>
