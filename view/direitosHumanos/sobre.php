        <h4 class="pt-2">Direitos da Mulher</h4>
        <p>Tem como missão elaborar, propor, articular, planejar e fomentar a implantação de políticas públicas para a defesa dos direitos das mulheres e equidade de gênero, assim como coordenar projetos, programas e campanhas para combater todas as formas de discriminação e preconceitos praticados contra as mulheres no município. Coordenará o Programa Municipal de Enfrentamento à Violência Doméstica e Familiar contra a mulher.</p>

        <h4 class="pt-2">Direitos da Pessoa com Deficiência</h4>
        <p>Tem como objetivo promover o protagonismo da pessoa com deficiência e sua efetiva participação na sociedade, a  valorização e respeito à diversidade humana, o fortalecimento da atuação em rede para resolução de questões relativas às pessoas com deficiência.</p>

