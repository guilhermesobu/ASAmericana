        <h4 class="pt-2">Diretos da Criança e do Adolescente</h4>
        <p>Tem por finalidade coordenar e acompanhar as ações de promoção, defesa e proteção dos direitos das crianças e dos adolescentes desenvolvidas no município, atuando junto às instâncias que formulam e executam políticas públicas voltadas a essa população, em consonância com o Estatuto da Criança e do Adolescente (ECA) e do Plano Nacional dos Direitos Humanos de Crianças e Adolescentes.</p>
        <p>( Acrescentar - Política Municipal de Promoção, Proteção e Defesa do Direito de Crianças e Adolescentes à Convivência Familiar e Comunitária-PMCFC, Plano Decenal de Atendimento Socioeducativo, Plano Decenal dos Direitos Humanos de Crianças e Adolescentes e Protocolo Municipal de Atendimento a Crianças e Adolescentes Vítimas ou testemunhas de violência). Apoia e dá suporte ao CMDCA e às suas Comissões técnicas. Mobilização de adolescentes para a promoção do protagonismo e participação sócio-política.</p>

        <h4 class="pt-2">Direitos da Pessoa Idosa</h4>
        <p class="mb-0">Tem como objetivo articular e integrar as políticas públicas para pessoas idosas junto aos órgãos da administração pública municipal. Promove a participação ativa da pessoa idosa na estruturação da rede de proteção e defesa dos direitos da pessoa idosa. Apoia e dá suporte ao Conselho Municipal do Idoso. <strong>Coordena o Programa São Paulo Amigo do Idoso</strong>.</p>
