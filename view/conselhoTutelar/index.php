        <h3 class="text-left">Conselho Tutelar</h3>
        <p>Conselho Tutelar é um órgão municipal responsável por zelar pelo cumprimento dos direitos da criança e do adolescente. Este foi criado juntamente com o ECA – Estatuto da Criança e do Adolescente, instituído pela Lei Federal nº 8.069 no dia 13 de julho de 1990.</p>
        <p>É um órgão permanente, não jurisdicional e autônomo nas suas ações conselheiras.</p>
        <p>O Conselho Tutelar é formado por 5 (cinco) membros eleitos pela comunidade para mandato de quatro anos.</p>
        <p>A competência legal do Conselho Tutelar está diretamente relacionada à aplicação das chamadas medidas de proteção à criança e ao adolescente, sempre que os direitos reconhecidos em Lei forem ameaçados ou violados por ação ou omissão da sociedade ou do Estado, por falta, omissão ou abuso dos pais ou responsável ou em razão de sua própria conduta (art. 98 ECA), inclusive nos casos de ato infracional praticado por criança abaixo de 12 anos (art. 105 ECA).</p>
        <p>Por isso, é dever de todos, sempre que se perceba abuso ou situações de risco contra crianças e adolescentes, como por exemplo, em casos de violência física, sexual ou emocional, comunicar o Conselho Tutelar.</p>
        <p>O atendimento às crianças e adolescentes e o aconselhamento aos pais ou responsáveis, com aplicação de medidas pertinentes a cada caso, tem o objetivo de reordenar e fortalecer o ambiente familiar e eliminar as situações de risco para as crianças e adolescentes.</p>
        <p>Cabe ressaltar que, o conselheiro tutelar somente aplica as medidas necessárias à proteção dos direitos da criança e/ou adolescente, ele não as executa, devendo por tanto, requisitar os serviços necessários para execução dessas medidas, ou seja, ao poder público através das políticas públicas.</p>

