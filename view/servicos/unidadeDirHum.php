        <h3>Conselho Tutelar</h3>

        <p>Este conselho foi criado conjuntamente ao <b>ECA – Estatuto da Criança e do Adolescente</b>, instituído pela Lei 8.069 no dia 13 de julho de 1990 sendo um órgão permanente, desta forma ,uma vez criado não pode ser extinto e com autonomia funcional, não sendo subordinado a qualquer outro órgão estatal. O Conselho Tutelar é um órgão municipal responsável por zelar pelos direitos da criança e do adolescente.</p>

        <p>O Conselho Tutelar é formado por membros eleitos pela comunidade para mandato de quatro anos. Nesse período, os conselheiros atendem crianças e adolescentes e aconselham seus pais e responsáveis. Seu trabalho é basicamente norteado sob denúncias, por isso, sempre que se perceba abuso ou situações de risco contra crianças e adolescentes, como por exemplo, em casos de violência física ou emocional, o Conselho Tutelar deve ser comunicado.</p>

        <p>Sua função é aplicar medidas que zelem pela proteção dos direitos da criança e do adolescente. Porém, o Conselho Tutelar não tem competência para aplicar medidas judiciais e não pode julgar nenhum caso e não age como órgão correcional. Assim, quando um adolescente, por exemplo, pratica algum ato infracional, este será direcionado à Polícia Militar. O Conselho Tutelar poderá atuar somente com aconselhamento aos responsáveis. Não é função do Conselho Tutelar, expedir autorização para viagens ou desfiles, determinar a guarda legal da criança. Entenda que o Conselho Tutelar é apenas um órgão zelador do direito.</p>

